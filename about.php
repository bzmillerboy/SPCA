            <h2 >Mission</h2>
            <p>SPCA Cincinnati is an animal shelter and humane society that continually strives to strengthen the human-animal bond and improve the welfare of animals by fostering the humane care and treatment of all animals. Through humane education and animal protection, we improve the quality of life in our community.</p>
            <p>Underlying the work at SPCA Cincinnati is the philosophy that how a community treats its animals is a reflection of how its members treat each other.&nbsp; The staff and volunteers of SPCA Cincinnati strive to not only give the animals in their care the best in humane treatment, but to continuously offer and develop programs to support those who own and care for animals.</p>
            <p>In addition to being the official agency that oversees stray dog control in the community, SPCA Cincinnati offers many programs and services, including animal adoptions, animal cruelty investigations, spay/neuter and veterinary services, companion animal and wildlife rescue, dog licensing, foster care program, obedience training, and the area’s most extensive humane education program.&nbsp; SPCA Cincinnati also works to introduce animals into the lives of people in our community, including those in hospitals, nursing homes, children’s homes, and schools, and works with victims of domestic violence to both provide shelter for their animals, and to help them rehabilitate.</p>

            <hr>


            <h2>History</h2>
            <p><img class="img-thumbnail img-wrap" src="images/spca-history.jpg" alt="SPCA History">Founded in 1873, SPCA Cincinnati has a long history of service to the Greater Cincinnati community.&nbsp; Originally founded as the Animal and Human Humane Society, it was the first organization of its kind in Ohio.&nbsp; Eventually two separate agencies evolved, leading to the creation of the Hamilton County Society for the Prevention of Cruelty to Animals, which was headquartered in downtown Cincinnati.&nbsp; The headquarters moved to Colerain Avenue in Northside in the 1930s, and the current building was constructed in 1964.&nbsp; Thanks to the generosity of the community, SPCA Cincinnati built the Sharonville Humane Center in 2008, and received Simmonds Farm as a donation in 2009.</p>
            <p>Today, SPCA Cincinnati remains the very epitome of true service to the people and animals in our community. SPCA Cincinnati takes in over 7,000 dogs and over 9,000 cats annually.&nbsp; In addition to providing shelter and rehabilitation to these animals in need, SPCA Cincinnati collaborates with government, law enforcement, and social service organizations in its ongoing effort to encourage humane principles and foster the human-animal bond.&nbsp;</p>


            <hr>


            <h2>Admissions</h2>
            <p>SPCA Cincinnati welcomes every companion animal in need. We are an open admissions shelter. There are other organizations that call themselves "no kill" that are more accurately defined as "limited admissions" shelters. Those organizations generally restrict the number of animals they take in.</p>
            <p>We take in ill and injured animals and many that are not immediate candidates for adoption. We receive pets that are no longer wanted, pets from people who can no longer care for them, as well as stray animals.</p>
            <p>We work hard to give second chances to every healthy and rehabilitatable animal. With your help, we can do better if you can volunteer to foster a dog or cat, adopt a pet, or volunteer in one of our facilities.</p>
            <p>Breed groups, rescues, and other organizations are welcome to participate as placement partners to help find homes for some harder to place animals. Gwen Williams at the Northside Shelter, 513-541-6100, ext. 1122, and Misty Hale at the Sharonville Shelter, 513-247-2033 will be happy to assist.</p>
            <p>More animals keep coming - an average of 44 a day. We would like to save more lives. Community education is a key component of our ongoing effort to save more lives.</p>
