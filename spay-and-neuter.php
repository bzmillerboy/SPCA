            <h3>Pet Overpopulation</h3>
            <p>Pet overpopulation is a serious problem. It costs the lives of millions of pets and costs communities millions of dollars a year.</p>
            <p>Nationwide, the number of pets entering animal shelters is estimated to be 6 to 8 million. Only about 30 percent of dogs and 2 – 5 percent of cats are reclaimed by owners. Only about half of those remaining are adopted to new homes. The number of pets euthanized in shelters across the country is 3 to 4 million. (Estimates provided by The Humane Society of the United States.)</p>
            <p>SPCA Cincinnati is working with other shelters and animal welfare organizations to reduce the number of unwanted litters that are born, increase the number of lost pets that are rehomed and increase the number of relinquished pets adopted to new homes.</p>

            <h4>Here are some of the things we are doing:</h4>
            <ul>
            	<li>We spay/neuter cats and dogs that we adopt to new homes – more than 7,000 surgeries every year. We can spay/neuter as early as 8 weeks of age.</li>
                <li>We provide humane education programs in schools, stressing the importance of spaying/neutering family pets.</li>
                <li>Working with community partners, we help create comprehensive solutions to the problem of pet overpopulation through various community initiatives.</li>
                <li>We collaborate with local, state and national organizations to save the lives of all healthy or treatable cats and dogs that.</li>
            </ul>

